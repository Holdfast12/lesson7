"""Библиотека с моими функциями"""

def greet(name):
    """Функция выводит приветственное сообщение для имени name"""
    print("Hello, " + name + ". Good morning!")


def say_goodbye(name):
    """Функция выводит прощальное сообщение для имени name"""
    print("Goodbye, " + name)